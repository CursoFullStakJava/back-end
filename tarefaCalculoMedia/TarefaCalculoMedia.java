public class TarefaCalculoMedia {

    public static void main(String args[]) {
        media();
    }

    public static void media() {
        int nota1 = 10;
        int nota2 = 8;
        int nota3 = 6;
        int nota4 = 4;

        float resultadoMedia = (nota1 + nota2 + nota3 + nota4) / 4;

        System.out.println(resultadoMedia);
    }

}
